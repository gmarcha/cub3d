int	ft_isprint(int c)
{
	if (c >= '0' && c <= '9')
		return (1);
	if (c >= 'A' && c <= 'Z')
		return (1);
	if (c >= 'a' && c <= 'z')
		return (1);
	if (c == ' ')
		return (1);
	if (c >= ' ' && c <= '~')
		return (1);
	return (0);
}
